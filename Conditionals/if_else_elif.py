# if statement
age = 18
if age >= 18:
    print("You are an adult.")

# if-else statement
age = 16
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# if-elif-else statement
age = 20
if age < 13:
    print("You are a child.")
elif age < 20:
    print("You are a teenager.")
else:
    print("You are an adult.")